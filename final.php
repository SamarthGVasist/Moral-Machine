<h2><center>About the District</center></h2>
<p>
Bangalore Urban district, also known as, Bangalore City district, is a district of the Indian state of Karnataka. It is surrounded by the Bangalore Rural district on the east and north, the Ramanagara district on the west and the Krishnagiri district of Tamil Nadu on the south. Bangalore Urban district came into being in 1986, with the partition of the erstwhile Bangalore into Bangalore Urban and Bangalore Rural districts. Bangalore Urban has five taluks: Yelahanka, Bangalore North, Bangalore East, Bangalore South and Anekal. The city of Bangalore is situated in the Bangalore Urban district. The district has 17 hoblies, 668 villages and 9 municipal corporations. There are five talukas; Electronics City is situated in Anekal Taluka. The district had a population of 6,537,124 of which 88.11% is urban as of 2001.As of Census 2011, its population has increased to 9,621,551, with a sex-ratio of 908 females/males, the lowest in the state and its density is 4,378 people per square km. According to the 2011 census Bangalore Urban district has a population of 9,621,551,[7] roughly equal to the nation of Belarus[8] or the US state of North Carolina.[9] This gives it a ranking of 3rd in India (out of a total of 640). The district has a population density of 4,378 inhabitants per square kilometre (11,340/sq mi). Its population growth rate over the decade 2001-2011 was 46.68%. Bangalore has a sex ratio of 908 females for every 1000 males, and a literacy rate of 88.48%.

